#include <unistd.h>

void	ft_print_comb2(void)
{
	int	i;
	int	k;

	i = 0;
	while (i <= 98)
	{
		k = i + 1;
		while (k <= 99)
		{
			write(1, &(char){i / 10 + '0'}, 1);
			write(1, &(char){i % 10 + '0'}, 1);
			write(1, " ", 1);
			write(1, &(char){k / 10 + '0'}, 1);
			write(1, &(char){k % 10 + '0'}, 1);
			if (!(i == 98 && k == 99))
				write(1, ", ", 2);
			k++;
		}
		i++;
	}
}
